    public static FrameRegistro getInstacia(Frame parent, boolean modal, Cliente jugador, IFacadaDeNegocio facadaDeControl) {
        if (pantallaRegistro == null) {
            pantallaRegistro = new FrameRegistro(parent, modal, jugador, facadaDeControl);
        }
        return pantallaRegistro;
    }
    
    public void asignarPersonaje() {        
        if(this.jRadioButton_Imagen1.isSelected()) {
            ((Jugador) this.jugador).setAvatar(new Avatar(this.jRadioButton_Imagen1.getIcon()));
        } else if(this.jRadioButton_Imagen2.isSelected()) {
            ((Jugador) this.jugador).setAvatar(new Avatar(this.jRadioButton_Imagen2.getIcon()));
        } else if(this.jRadioButton_Imagen3.isSelected()) {
            ((Jugador) this.jugador).setAvatar(new Avatar(this.jRadioButton_Imagen3.getIcon()));
        } else if(this.jRadioButton_Imagen4.isSelected()) {
            ((Jugador) this.jugador).setAvatar(new Avatar(this.jRadioButton_Imagen4.getIcon()));
        }
    }
    
    public void mostrarMensaje(String mensaje, String titulo, int tipoDeMensaje) {
        JOptionPane.showMessageDialog(this, mensaje, titulo, tipoDeMensaje);
    }
