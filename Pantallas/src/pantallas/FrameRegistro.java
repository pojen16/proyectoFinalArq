    private void jButton_RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_RegistrarActionPerformed
        //Validar textFields
        
        ((Jugador) this.jugador).setNombre(jTextField1.getText().trim());
        this.asignarPersonaje();
        try {
            this.facadaDeControl.guardarJugador(this.jugador);
            this.mostrarMensaje("Has sido registrado correctamente "+ ((Jugador) this.jugador).getNombre(), "Mensaje", JOptionPane.INFORMATION_MESSAGE);
            this.setVisible(false);
        } catch (Exception ex) {
            this.mostrarMensaje(ex.getMessage(), "Mensaje", JOptionPane.OK_OPTION);
            ((Jugador) this.jugador).setAvatar(null);
            ((Jugador) this.jugador).setNombre(null);
        }
        
    }//GEN-LAST:event_jButton_RegistrarActionPerformed

